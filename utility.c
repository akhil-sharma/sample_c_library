#include "utility.h"

/*find the sum of two numbers*/
int add(int a, int b){
	return (a + b);
}

/*find the difference in two numbers*/
int subtract(int a, int b){
	return (a - b);
}

/*find the product of two numbers*/
int multiply(int a, int b){
	return (a * b);
}

/*divide the first number by second*/
int divide(int a, int b){
	return (a / b);
}